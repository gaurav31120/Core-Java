class ElseIfConditionals {
    public static void main(String[] args) {
        int x = 10;

        if (x > 10) {
            System.out.println("Greater than 10");
        } else if (x == 10) {
            System.out.println("Equal to 10");
        } else {
            System.out.println("Smaller than 10");

        }
    }
}
